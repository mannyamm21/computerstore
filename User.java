    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        try {
            Class.forName("java.sql.DriverManager");
            java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/computer_retailer", "root", "1234");
            java.sql.Statement stm = con.createStatement();
            String un = User_Name.getText();
            String up = User_Password.getText();
            String upn = User_PhoneNo.getText();
            String st = "insert into user_table values('"+un+"','"+up+"','"+upn+"')";
            stm.executeUpdate(st);
            JOptionPane.showMessageDialog(this, "Data Saved");
            con.close();
            Select();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_add_btnActionPerformed

    private void delete_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_btnActionPerformed
        if (User_Name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "The table is empty");
        } else {
            try {
                Class.forName("java.sql.DriverManager");
                java.sql.Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/computer_retailer", "root", "1234");
                java.sql.Statement stm = con.createStatement();
                String id = User_Name.getText();
                String Query = "delete from User_table where User_Name="+"'"+id+"'";
                stm.executeUpdate(Query);
                Select();
                JOptionPane.showMessageDialog(this, "User Delected Successfully");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_delete_btnActionPerformed

